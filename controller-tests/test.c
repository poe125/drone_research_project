// CRC8 helper
uint8_t crsf_crc8(uint8_t *data, uint8_t len) {
    uint8_t crc = 0;
    for (uint8_t i = 0; i < len; i++)
        crc = crsf_crc_table[crc ^ data[i]];
    return crc;
}

// Function to build and send CRSF RC frame
void send_crsf_rc_channels(int fd) {
    uint8_t frame[32];
    uint16_t channels[16];
    memset(channels, 992, sizeof(channels)); // 992 = ~1500 µs mid

    // Set AUX5 high (2000 µs)
    channels[8] = 2011;

    // Pack 11-bit channels (per CRSF spec)
    uint8_t payload[22];
    uint32_t bitbuf = 0;
    int bitcount = 0;
    int p = 0;
    for (int i = 0; i < 16; i++) {
        bitbuf |= ((uint32_t)channels[i] & 0x7FF) << bitcount;
        bitcount += 11;
        while (bitcount >= 8) {
            payload[p++] = bitbuf & 0xFF;
            bitbuf >>= 8;
            bitcount -= 8;
        }
    }

    // Build frame
    uint8_t type = CRSF_FRAMETYPE_RC_CHANNELS_PACKED;
    uint8_t length = sizeof(payload) + 2; // Type + CRC
    int i = 0;
    frame[i++] = CRSF_ADDRESS_MODULE;
    frame[i++] = length;
    frame[i++] = type;
    memcpy(&frame[i], payload, sizeof(payload));
    i += sizeof(payload);
    frame[i++] = crsf_crc8(&frame[2], length - 1);

    // Send
    write(fd, frame, i);
    tcdrain(fd);
    printf("CRSF RC frame sent (%d bytes)\n", i);
}

int main() {
    int fd;
    struct termios options;

    fd = open(SERIAL_PORT, O_RDWR | O_NOCTTY | O_NDELAY);
    if (fd == -1) {
        perror("Unable to open serial port");
        return 1;
    }

    fcntl(fd, F_SETFL, 0);
    tcgetattr(fd, &options);
    cfsetispeed(&options, BAUDRATE);
    cfsetospeed(&options, BAUDRATE);
    options.c_cflag |= (CLOCAL | CREAD);
    options.c_cflag &= ~PARENB;
    options.c_cflag &= ~CSTOPB;
    options.c_cflag &= ~CSIZE;
    options.c_cflag |= CS8;
    tcsetattr(fd, TCSANOW, &options);

    send_crsf_rc_channels(fd);
    close(fd);
    return 0;
}

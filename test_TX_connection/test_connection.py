def crsf_crc8(data):
    crc = 0
    for b in data:
        crc = CRC8_TABLE[crc ^ b]
    return crc

def create_rc_frame(channels):
    values = [int((ch - 988) * 820 / 1012) for ch in channels]  # map to 0‚Äì820
    packed = bytearray(22)
    bitbuf = 0
    bitcount = 0
    byte_i = 0

    for val in values:
        bitbuf |= (val & 0x7FF) << bitcount
        bitcount += 11
        while bitcount >= 8 and byte_i < 22:
            packed[byte_i] = bitbuf & 0xFF
            byte_i += 1
            bitbuf >>= 8
            bitcount -= 8

    frame = bytearray()
    frame.append(0xC8)   # Address
    frame.append(24)     # Length (TYPE + PAYLOAD + CRC)
    frame.append(0x16)   # Type: RC Channels
    frame.extend(packed)
    frame.append(crsf_crc8(frame[2:]))  # CRC
    return frame


